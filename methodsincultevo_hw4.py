import numpy as np

def transform_value(x):
    # 1. Handle NaN (covers np.nan and float nan)
    if isinstance(x, float) and np.isnan(x):
        return np.nan

    # 2. Handle numbers (int, float but not nan)
    if isinstance(x, (int, float)):
        return float(x)

    # 3. Handle strings
    if isinstance(x, str):
        x = x.strip()

        # Special replacement
        if x == "Other [specify in evidence_quotes ]":
            return "Other"

        # Split comma-separated values
        if "," in x:
            return {item.strip() for item in x.split(",") if item.strip()}

        return {x}

    # Fallback (just return as is)
    return x


def transform_array(arr):
    vectorized = np.vectorize(transform_value, otypes=[object])
    return vectorized(arr).T

