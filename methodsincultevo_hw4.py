# This function takes the source ids from the different codders sheets, and then creates a unique set that contains all coded ressources by an ID
# Expected format of the input is the list of the pages of the sheets, where each sheet is the sheet WiFa,WiMo,HuFa,HuMo of each coder
def IC_Sources_Set(frames):
  B = set()
  for frame in frames:
    # Clean 'Source_ID' values: convert to string, strip whitespace, normalize, and filter out 'nan' strings
    #Caution: This is what a Gemini proposed, as in my code without cleaning, as every member of the groups used different type of sheets documents
    # there was the error with creating different source ids..
    cleaned_source_ids = frame['Source_ID'].astype(str).str.lower().str.replace(r'\s+', ' ', regex=True).str.strip()
    cleaned_source_ids = cleaned_source_ids[cleaned_source_ids != 'nan']
    S = set(cleaned_source_ids.values)
    B = B.union(S)
  # Only remove 'column h on source_database' (after cleaning) if it exists in the set
  if 'column h on source_database' in B:
    B.remove('column h on source_database')
  return B
