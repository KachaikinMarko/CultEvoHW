# This function takes an individual codder sheet and the mutual of all codders sources list,
# And creates a new sheet that would containt all the sources and add the values None If the ressource was not codded by a codder
#sources_list is expected to be a set of sources as in an output from IC_Sources_Set

def IC_IndSheet(frame,sources_list):
  # Create a copy to avoid SettingWithCopyWarning
  frame_copy = frame.copy()
  # Clean 'Source_ID' column to ensure uniqueness and consistency
  frame_copy['Source_ID'] = frame_copy['Source_ID'].astype(str).str.lower().str.replace(r'\s+', ' ', regex=True).str.strip()
  # Filter out rows where 'Source_ID' became 'nan' string (from actual NaNs)
  frame_cleaned = frame_copy[frame_copy['Source_ID'] != 'nan']

  # Now drop duplicates based on the cleaned 'Source_ID' and set it as index
  frame_unique_sources = frame_cleaned.drop_duplicates(subset=['Source_ID']).set_index('Source_ID')

  result = (
    frame_unique_sources
      .reindex(sources_list)
      .reset_index()
)
  return result

