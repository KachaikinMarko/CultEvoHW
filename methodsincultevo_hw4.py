def IC_Sheet(frames,variable_name):
  sources_list = IC_Sources_Set(frames)
  new_frames=[]
  for frame in frames:
    new_frames.append(IC_IndSheet(frame,sources_list))
  new_frames = [
    df.set_index('Source_ID').rename(columns={variable_name: f"Coder_{i+1}"})
    for i, df in enumerate(new_frames)
]

  merged = pd.concat(new_frames, axis=1).reset_index()
  return merged

