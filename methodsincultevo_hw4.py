import numpy as np
from collections import Counter
import math

def krippendorff_alpha_nominal(data):
    """
    data: 2D numpy array (coders x items)
          entries can be any hashable category or np.nan
    """

    data = np.array(data, dtype=object)

    # Get all valid categories
    categories = set()
    for val in data.flatten():
        if val is not None and not (isinstance(val, float) and np.isnan(val)):
            categories.add(val)

    categories = list(categories)

    # Coincidence matrix
    coincidence = {c: Counter() for c in categories}

    for col in range(data.shape[1]):
        values = [v for v in data[:, col]
                  if v is not None and not (isinstance(v, float) and np.isnan(v))]

        n = len(values)
        if n < 2:
            continue

        for i in range(n):
            for j in range(n):
                if i != j:
                    coincidence[values[i]][values[j]] += 1

    # Observed disagreement Do
    Do = 0
    total_pairs = 0

    for c in categories:
        for k in categories:
            if c != k:
                Do += coincidence[c][k]
            total_pairs += coincidence[c][k]

    if total_pairs == 0:
        return np.nan

    Do = Do / total_pairs

    # Expected disagreement De
    marginals = Counter()
    for c in categories:
        marginals[c] = sum(coincidence[c].values())

    total = sum(marginals.values())

    De = 0
    for c in categories:
        for k in categories:
            if c != k:
                De += marginals[c] * marginals[k]

    De = De / (total * (total - 1)) if total > 1 else 0

    if De == 0:
        return 1.0

    return 1 - (Do / De)

def normalize_multilabel(data):
    out = np.empty_like(data, dtype=object)

    for i in range(data.shape[0]):
        for j in range(data.shape[1]):
            val = data[i, j]

            if isinstance(val, set):
                out[i, j] = tuple(sorted(val))  # hashable
            else:
                out[i, j] = val

    return out

